package Compocicion;

public class Punto {
    private int x, y;
    /**
    * @param x 
    * @param y 
    */
    public Punto(int x, int y) {
    this.x = x;
    this.y = y;
    }
    /**
    * @return
    */
    public int getX() {
    return x;
    }
    /**
    * @param Parámetro 
    */
    public void setX(int x) {
    this.x = x;
    }
    /**
    * @return 
    */
    public int getY() {
    return y;
    }
    /**
    * @param Parámetro 
    */
    public void setY(int y) {
    this.y = y;
    }
}
